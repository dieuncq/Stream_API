public class StudentDTO {
    public Integer i_ID;
    public String s_Name;
    public String s_Gender;
    public Integer i_Age;

    public StudentDTO(Integer i_ID, String s_Name, String s_Gender, Integer i_Age) {
        this.i_ID = i_ID;
        this.s_Name = s_Name;
        this.s_Gender = s_Gender;
        this.i_Age = i_Age;
    }
}
